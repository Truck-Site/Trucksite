$(function () {
  // Accordion arrows
  $('#accordion .collapse').on('show.bs.collapse', function () {
    $(this).siblings('.questions__header').addClass('active');
  });

  $('#accordion .collapse').on('hide.bs.collapse', function () {
    $(this).siblings('.questions__header').removeClass('active');
  });
});

